def test_queue_operations():
    """Test SQLite queue enqueue/dequeue/complete cycle."""
    print("\n=== TEST: Queue enqueue → dequeue → complete ===")
    q = TaskQueue(db_path=":memory:")

    task_id = q.enqueue("sales_outreach", {"company": "TestCorp"}, client="test_client", priority=5)
    assert task_id, "enqueue returned empty task_id"

    task = q.dequeue()
    assert task, "dequeue returned None"
    assert task["task_id"] == task_id

    # Verify the DB was updated to running
    refetched = q.get(task_id)
    assert refetched["status"] == "running", f"Expected 'running', got '{refetched['status']}'"

    q.complete(task_id, outputs={"result": "ok"}, qa_status="PASS", cost_usd=0.05)
    done = q.get(task_id)
    assert done["status"] == "completed"
    assert done["qa_status"] == "PASS"

    # Test fail path
    task_id2 = q.enqueue("support_ticket", {"ticket": "help"}, client="test_client")
    q.dequeue()
    q.fail(task_id2, error="LLM timeout")
    failed = q.get(task_id2)
    assert failed["status"] == "failed"
    assert failed["error"] == "LLM timeout"

    stats = q.stats()
    assert stats["completed"] == 1
    assert stats["failed"] == 1
    print(f"  PASS: enqueue→dequeue→complete/fail cycle works. Stats: {stats}")
    return True


